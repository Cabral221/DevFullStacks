    {
        // dd($topic);
        $com = new CommentsForum;
        $com->user_id = $comment->user()->id;
        $com->comment = $comment->comment;
        $topic->comments()->save($com);
      
        Flashy::message("Merci de votre commentaire");
        return redirect()->route('forum.show',$topic);
    }

